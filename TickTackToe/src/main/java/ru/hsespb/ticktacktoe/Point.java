package ru.hsespb.ticktacktoe;

/** Stores 2-dimension vector with int coordinates */
public class Point {
    private int x;
    private int y;

    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
